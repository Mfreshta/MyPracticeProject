package javaBasics;

public class If_Statements {

	@SuppressWarnings("unused")
	public static void main(String[] args) {

		// if Statement is Used to execute a peace of code based on a condition
		// Syntax: if(boolean-expression) ===> booleana-expression = condition
		// Statement===> System.out.println();

		if (true) {
			System.out.println("Inside the if statement");

			System.out.println("inside the if statment");

			/*
			 * If you want to have more than one if statement you need to have{} after each
			 * if statement
			 */
			/*
			 * The statemnt(s) are executd if the boolean-expression evaluates to true if
			 * the boolean-expression evaluate to false the statment(s) will be skipped.
			 */

		}
		System.out.println("outside the if statement");

		if (false) {
		System.out.println("Inside the if statement");

		System.out.println("outside the if statment");
	}
}
}